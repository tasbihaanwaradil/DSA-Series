#include <iostream>
using namespace std;

int main(){
    int num1;
    int num2;

    cin >> num1 >> num2;
    cout << "Sum = " << (num1 + num2) << endl;
    cout << "Difference = " << (num1 - num2) << endl;
    cout << "Multiplication = " << (num1 * num2) << endl;
    cout << "Divison = " << (num1 / num2) << endl;
    cout << "Modulo = " << (num1 % num2) << endl;
    return 0;
}